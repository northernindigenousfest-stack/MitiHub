  // Northern Kenya Earth Palette - Natural tones optimized for outdoor mobile use
  static const Color primaryLight = Color(0xFF2D5A27); // Deep forest green
  static const Color primaryVariantLight =
      Color(0xFF1E3D1A); // Darker forest green
  static const Color secondaryLight = Color(0xFF8B4513); // Rich earth brown
  static const Color secondaryVariantLight =
      Color(0xFF654321); // Darker earth brown
  static const Color accentLight = Color(0xFFF4A460); // Warm sandy tone
  static const Color successLight = Color(0xFF228B22); // Natural leaf green
  static const Color warningLight = Color(0xFFDAA520); // Golden yellow
  static const Color errorLight = Color(0xFFCD5C5C); // Muted red
  static const Color backgroundLight = Color(0xFFFAFAFA); // Clean off-white
  static const Color surfaceLight = Color(0xFFFFFFFF); // Pure white
  static const Color textPrimaryLight = Color(0xFF2C2C2C); // Dark charcoal
  static const Color textSecondaryLight = Color(0xFF666666); // Medium gray

  // Dark theme variations maintaining the earth palette
  static const Color primaryDark =
      Color(0xFF4A8B3A); // Lighter forest green for dark mode
  static const Color primaryVariantDark =
      Color(0xFF2D5A27); // Original primary as variant
  static const Color secondaryDark = Color(0xFFB8860B); // Lighter earth brown
  static const Color secondaryVariantDark =
      Color(0xFF8B4513); // Original secondary as variant
  static const Color accentDark = Color(0xFFF4A460); // Same sandy tone
  static const Color successDark = Color(0xFF32CD32); // Brighter leaf green
  static const Color warningDark = Color(0xFFFFD700); // Brighter golden yellow
  static const Color errorDark = Color(0xFFDC143C); // Brighter muted red
  static const Color backgroundDark = Color(0xFF121212); // Dark background
  static const Color surfaceDark = Color(0xFF1E1E1E); // Dark surface
  static const Color textPrimaryDark = Color(0xFFE0E0E0); // Light text
  static const Color textSecondaryDark = Color(0xFFB0B0B0); // Medium light text

  // Additional semantic colors
  static const Color cardLight = Color(0xFFF8F8F8);
  static const Color cardDark = Color(0xFF2A2A2A);
  static const Color dividerLight = Color(0xFFE0E0E0);
  static const Color dividerDark = Color(0xFF404040);
  static const Color shadowLight = Color(0x33000000); // 20% opacity
  static const Color shadowDark = Color(0x33FFFFFF); // 20% opacity

  /// Light theme implementing Northern Kenya Earth Palette
  static ThemeData lightTheme = ThemeData(
    brightness: Brightness.light,
    colorScheme: ColorScheme(
      brightness: Brightness.light,
      primary: primaryLight,
      onPrimary: Colors.white,
      primaryContainer: primaryVariantLight,
      onPrimaryContainer: Colors.white,
      secondary: secondaryLight,
      onSecondary: Colors.white,
      secondaryContainer: secondaryVariantLight,
      onSecondaryContainer: Colors.white,
      tertiary: accentLight,
